-- ------------------------------------------------------------
-- 9. Trigger: enforce tournament status transitions
--    Allowed transitions:
--      open    → ready    ✓
--      ready   → open     ✓ (reversible)
--      ready   → started  ✓
--      started → ended    ✓
--    Forbidden:
--      started → open     ✗
--      started → ready    ✗
--      ended   → *        ✗
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION enforce_tournament_status_transition()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  -- No-op if status didn't change
  IF NEW.status = OLD.status THEN
    RETURN NEW;
  END IF;

  -- Once started, can only move to ended
  IF OLD.status = 'started' AND NEW.status <> 'ended' THEN
    RAISE EXCEPTION
      'Cannot transition tournament status from started to %. Once started, a tournament can only be ended.',
      NEW.status;
  END IF;

  -- Once ended, no further transitions allowed
  IF OLD.status = 'ended' THEN
    RAISE EXCEPTION
      'Cannot transition tournament status from ended. The tournament is finished.';
  END IF;

  RETURN NEW;
END;
$$;

CREATE TRIGGER enforce_tournament_status_transition
  BEFORE UPDATE OF status ON tournaments
  FOR EACH ROW
  EXECUTE FUNCTION enforce_tournament_status_transition();
